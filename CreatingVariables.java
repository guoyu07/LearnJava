public class CreatingVariables {
	public static void main( String[] args ) {
		int x, y, age;
		double seconds, e, checking;
		String firstName, lastName, title;

		x = 10;
		y = 400;
		age = 39;

		seconds = 4.71;
		e = 2.71828;
		checking = 1.89;

		firstName = "Vivek";
		lastName = "Ratkalkar";
		title = "Mr.";

		System.out.println("variable x contains" + x);
		System.out.println("variable y contains" + y);
		
	}
}